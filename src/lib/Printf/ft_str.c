#include "../../../includes/libft.h"

int	ft_str(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		ft_c(str[i]);
		i++;
	}
	return (i);
}
